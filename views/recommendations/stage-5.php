        <div class="recommendation-content">
        
          <h2>Recommendations</h2>
          
          <h3>Aquisition</h3>
          <p>Treat all consumer interactions with extreme sensitivity, care and authenticity. Communicate tactics that make it easy for the customer to switch providers:</p>
          <ul>
            <li>Empower potential new customers with open and honest communication. Take time to clearly explain contract and billing to clearly set expectations.</li>
            <li>Offer tactics that facilitate the switching process such as: Pay termination fee, self installation, specific installation time, shorter installation window, return of existing equipment to previous providers, free education upon installation</li>
          </ul>
          
          <h3>Retention</h3>
          <p>Initial experiences are critical in determining future expectations. Experiences that make consumers feel confident and validated in their decision, result in more positive, long-term provider expectations moving forward.</p>
          <ul>
            <li>Smooth installation process: Arrive on time, quick set up, education / intro to features</li>
            <li>Excellent customer service</li>
          </ul>
        
