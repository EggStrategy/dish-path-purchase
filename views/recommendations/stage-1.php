          <h2>Recommendations</h2>
        
          <h3>Acquisitions</h3>
          <p>Spark interest among potential new customers through promotions that are connected to more meaningful and differentiating benefits.</p>
          <ul>
            <li>Give promotions a reason for being, by connecting them to the ability for consumers to experience joy, connection and escape through TV.</li>
          </ul>
          
          <h3>Retention</h3>
          <p>Show existing customers that they are valued before it is too late:</p>
          <ul>
            <li>Communicate upcoming changes to existing customers prior to the contract expiration, along with applicable promotions.</li>
            <li>Regularly offer loyalty rewards, such as extra channels or automatic technology upgrades.</li>
            <li>Offer concessions to existing customers early on, before they have a chance to strongly consider alternate options.</li>
          </ul>
        
